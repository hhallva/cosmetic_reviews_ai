from abc import ABC, abstractmethod
from typing import List, Any


class BaseSearchService(ABC):
    """
        Абстрактный базовый класс для сервисов поиска.

        Определяет единый контракт, который обязаны реализовывать все конкретные
        поисковые модули (Google, Yandex, внутренние API, парсеры, БД и т.д.).
        Класс нельзя инстанцировать напрямую — используется только как основа для наследования.
    """

    @abstractmethod
    def search(self, product_name: str, max_results: int) -> List[Any]:
        """
        Выполняет поиск информации по названию товара.

        Args:
            product_name (str): Поисковый запрос / название товара.
            max_results (int): Максимальное количество результатов для возврата.

        Returns:
            List[Any]: Список найденных объектов. Тип элемента зависит от конкретной реализации.

        Raises:
            NotImplementedError: Если метод не переопределён в дочернем классе.
            ValueError: Если `max_results` < 1 или `product_name` пустой (опционально, на уровне реализации).

        Notes:
            Наследники должны самостоятельно обрабатывать:
            • Сетевые запросы и таймауты
            • Парсинг / маппинг ответов в единый формат
            • Кеширование и ретраи при ошибках
        """
